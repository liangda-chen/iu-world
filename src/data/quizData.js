export const getRandomQuestions = () => {
  // 創建題目副本以避免修改原陣列
  const questionsCopy = [...allQuizQuestions];
  const selectedQuestions = [];
  
  // 確保選取的題目數量不超過題庫總數
  const count = Math.min(10, questionsCopy.length);
  
  for (let i = 0; i < count; i++) {
    // 從剩餘題目中隨機選取
    const randomIndex = Math.floor(Math.random() * questionsCopy.length);
    const selectedQuestion = questionsCopy.splice(randomIndex, 1)[0];
    
    // 隨機排列選項順序
    const shuffledOptions = shuffleArray([...selectedQuestion.options]);
    
    // 找出正確答案在新選項中的位置
    const newCorrectAnswer = shuffledOptions.find(
      option => option === selectedQuestion.correctAnswer
    );
    
    selectedQuestions.push({
      ...selectedQuestion,
      options: shuffledOptions,
      correctAnswer: newCorrectAnswer
    });
  }
  
  return selectedQuestions;
};

// Fisher-Yates洗牌算法
function shuffleArray(array) {
  const newArray = [...array];
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
  }
  return newArray;
}